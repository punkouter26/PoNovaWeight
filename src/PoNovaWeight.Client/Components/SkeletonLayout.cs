namespace PoNovaWeight.Client.Components;

/// <summary>
/// Layout variants for the unified SkeletonLoader component.
/// </summary>
public enum SkeletonLayout
{
    /// <summary>
    /// Full dashboard layout with multiple cards.
    /// </summary>
    Dashboard,

    /// <summary>
    /// Single card skeleton.
    /// </summary>
    Card,

    /// <summary>
    /// Compact day card for list items.
    /// </summary>
    DayCard,

    /// <summary>
    /// Calendar grid layout.
    /// </summary>
    Calendar,

    /// <summary>
    /// Generic/custom skeleton with configurable dimensions.
    /// </summary>
    Custom
}
